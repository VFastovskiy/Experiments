#include "libft.h"

void ft_break()
{
    int i;

    for(i = 10; i <= 20; i++)
    {
        if (i == 17)
            break ;
        else
        {
            ft_putnbr(i);
            ft_putchar('\n');
        }
        
    }
}

int main()
{
    ft_break();
    return (0);
}